#include <stdlib.h>

int	ft_atoi(const char *s)
{
	int	neg;
	int	res;

	neg = 0;
	res = 0;
	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s++ == '-')
			neg = 1;
	}
	while (*s >= '0' && *s <= '9')
	{
		if ((res > 2147483647 / 10) && ((*s - '0') > 7))
		{
			if (neg)
				return (-2147483648);
			return (2147483647);
		}
		res = res * 10 + (*s++ - '0');
	}
	if (neg)
		res = -res;
	return (res);
}

